"""
M2.3 metrics.py — 评价指标体系

指标：
- 年化收益率
- 最大回撤
- 夏普比率
- 卡玛比率
- 胜率
- 盈亏比
- 交易次数
"""

import numpy as np
import pandas as pd
from typing import List


def 计算年化收益率(总收益率: float, 天数: int) -> float:
    """年化收益率 = (1+总收益率)^(252/天数) - 1"""
    if 天数 <= 0:
        return 0.0
    return (1 + 总收益率) ** (252 / 天数) - 1


def 计算夏普比率(日收益率序列: List[float], 无风险利率: float = 0.03) -> float:
    """夏普比率 = (年化收益率 - 无风险利率) / 年化波动率"""
    if len(日收益率序列) < 2:
        return 0.0
    日收益率 = np.array(日收益率序列)
    超额收益 = 日收益率 - 无风险利率 / 252
    年化超额 = np.mean(超额收益) * 252
    年化波动 = np.std(日收益率) * np.sqrt(252)
    if 年化波动 == 0:
        return 0.0
    return 年化超额 / 年化波动


def 计算最大回撤(净值序列: List[float]) -> float:
    """最大回撤 = max(1 - 当前净值/前最高净值)"""
    if len(净值序列) == 0:
        return 0.0
    净值 = np.array(净值序列)
    峰值 = np.maximum.accumulate(净值)
    回撤 = (净值 - 峰值) / 峰值
    return abs(min(回撤)) if len(回撤) > 0 else 0.0


def 计算卡玛比率(总收益率: float, 天数: int, 最大回撤: float) -> float:
    """卡玛比率 = 年化收益率 / 最大回撤"""
    年化 = 计算年化收益率(总收益率, 天数)
    if 最大回撤 == 0:
        return 0.0
    return 年化 / 最大回撤


def 计算胜率(交易记录: List[dict]) -> dict:
    """
    统计胜率/盈亏比

    Args:
        交易记录: [{"方向":"买入", "盈亏":浮点数, "天数":持仓天数}]

    Returns:
        dict
    """
    总次数 = len(交易记录)
    if 总次数 == 0:
        return {
            "总次数": 0, "胜率": 0, "盈亏比": 0,
            "盈利次数": 0, "亏损次数": 0,
            "总盈利": 0, "总亏损": 0,
        }

    盈利 = [t for t in 交易记录 if t.get("盈亏", 0) > 0]
    亏损 = [t for t in 交易记录 if t.get("盈亏", 0) < 0]

    总盈利 = sum(t["盈亏"] for t in 盈利)
    总亏损 = abs(sum(t["盈亏"] for t in 亏损))

    return {
        "总次数": 总次数,
        "胜率": len(盈利) / 总次数 if 总次数 > 0 else 0,
        "盈亏比": 总盈利 / 总亏损 if 总亏损 > 0 else float("inf"),
        "盈利次数": len(盈利),
        "亏损次数": len(亏损),
        "总盈利": 总盈利,
        "总亏损": 总亏损,
    }


def 计算完整指标(净值序列: List[float], 交易记录: List[dict], 天数: int) -> dict:
    """
    计算所有指标

    Args:
        净值序列: 每日净值列表（从1开始）
        交易记录: 每笔交易的盈亏记录
        天数: 回测天数

    Returns:
        dict: 包含所有指标
    """
    if len(净值序列) < 2:
        return {"错误": "数据不足"}

    总收益率 = (净值序列[-1] - 净值序列[0]) / 净值序列[0]

    # 日收益率序列
    日收益率 = [0]
    for i in range(1, len(净值序列)):
        日收益率.append((净值序列[i] - 净值序列[i - 1]) / 净值序列[i - 1])

    最大回撤 = 计算最大回撤(净值序列)
    年化 = 计算年化收益率(总收益率, 天数)
    夏普 = 计算夏普比率(日收益率)
    卡玛 = 计算卡玛比率(总收益率, 天数, 最大回撤)
    胜率统计 = 计算胜率(交易记录)

    return {
        "总收益率": 总收益率,
        "年化收益率": 年化,
        "最大回撤": 最大回撤,
        "夏普比率": 夏普,
        "卡玛比率": 卡玛,
        **胜率统计,
        "回测天数": 天数,
        "结束净值": 净值序列[-1],
    }


