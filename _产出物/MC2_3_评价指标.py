    print(f"  总交易次数:    {指标.get('总次数', 'N/A'):>8}")
    print(f"  胜率:          {指标.get('胜率', 'N/A'):>8.1%}")
    print(f"  盈亏比:        {指标.get('盈亏比', 'N/A'):>8.2f}")
    print(f"  盈利次数:      {指标.get('盈利次数', 'N/A')}")
