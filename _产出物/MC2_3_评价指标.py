    print(f"  最大回撤:      {指标.get('最大回撤', 'N/A'):>8.2%}")
    print(f"  夏普比率:      {指标.get('夏普比率', 'N/A'):>8.2f}")
    print(f"  卡玛比率:      {指标.get('卡玛比率', 'N/A'):>8.2f}")
