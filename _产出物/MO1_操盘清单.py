"""
MO1 每日操盘清单输出 — 整合MC+MP输出，生成可执行的操盘清单
"""

from datetime import date


def 生成操盘清单(持仓列表: list, 候选列表: list,
                 板块仓位: dict, 调整建议: list,
                 预警事件: list) -> str:
    """
    整合所有信息，生成每日操盘清单

    Args:
        持仓列表: 当前持仓信息
        候选列表: 候选推荐标的
        板块仓位: {板块: 仓位%}
        调整建议: MP2输出的调整方案
        预警事件: MP5输出的预警

    Returns:
        格式化的操盘清单文本
    """
    清单 = []
    清单.append(f"【今日操盘清单】{date.today()}\n")

    # 已有持仓操作
    清单.append("── 已有持仓操作 ──")
    for 持仓 in 持仓列表:
        清单.append(
            f"{持仓['名称']}({持仓['代码']}) "
            f"[{持仓['板块']}] "
            f"区划={持仓['区划']} "
            f"日层段={持仓['日层段']} "
            f"→ {持仓['操作建议']}（{持仓['原因']}）")

    # 板块仓位监控
    清单.append("\n── 板块仓位监控 ──")
    for 板块, 仓位 in 板块仓位.items():
        状态 = "正常"
        if 仓位 > 25:
            状态 = "⚠️ 接近上限"
        if 仓位 > 30:
            状态 = "🔴 超限"
        清单.append(f"{板块}：{仓位:.1f}%（上限30%）→ {状态}")

    # 候选推荐
    if 候选列表:
        清单.append("\n── 候选推荐 ──")
        for 候选 in 候选列表:
            清单.append(
                f"{候选['名称']}({候选['代码']}) "
                f"区划={候选['区划']} → 可开{候选['策略']}")

    # 预警
    if 预警事件:
        清单.append("\n── 预警 ──")
        for 预警 in 预警事件:
            清单.append(f"[{预警['优先级']}] {预警['原因']}")

    return "\n".join(清单)