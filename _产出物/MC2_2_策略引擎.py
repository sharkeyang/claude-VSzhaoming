"""
M2.2 strategy.py — 三层策略引擎

依据：M1.3_三层策略伪代码.md
三层：策略一(基仓长期持有) + 策略二(下周冲高) + 策略三(下日冲高)

PSL计算说明：
- 使用"涨幅"列（每日涨跌幅%）
- 初始收盘价用第一个数据日的"结价_周前"作为估算起点
- 每日收盘价 = 前日收盘价 × (1 + 涨幅/100)
- 持仓盈亏 = (当日收盘价 - 买入价) / 买入价 × 持仓金额
"""

