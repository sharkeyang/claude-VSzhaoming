@dataclass
class 持仓状态:
    """一个标的的持仓模拟"""
    日期: object = None
    有基仓: bool = False
    基仓买入价: float = 0.0
    基仓量: int = 0
    基仓持仓天数: int = 0
    有下周冲高: bool = False
    下周冲高买入价: float = 0.0
    下周冲高买入日: object = None
    下周冲高量: int = 0
    下周最高价: float = 0.0  # 追踪买入后最高价
    下周已止盈: bool = False
    有下日冲高: bool = False
    下日冲高买入价: float = 0.0
    下日冲高买入日: object = None
    下日冲高量: int = 0

    已实现盈亏: float = 0.0
    交易次数: int = 0
    盈利次数: int = 0
    交易记录: list = field(default_factory=list)  # 每笔交易的详情

    # 净值跟踪
    初始资金: float = 50000
    当前资金: float = 50000
    持仓市值: float = 0.0
    每日净值: list = field(default_factory=list)

    操作日志: list = field(default_factory=list)

    def log(self, msg: str):
        self.操作日志.append(msg)


