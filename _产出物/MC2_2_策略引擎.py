def 通过前置过滤(row) -> bool:
    """M1.3 §二 准入条件"""
    if not row["WXCD合格"]:
        return False
    if not row["前周WXZC>0"]:
        return False
    if not row["今日DXZE>0"]:
        return False
    if not row["DXZD>0"]:
        return False
    return True


# ============================================================
# 日诱甄别
# ============================================================

def 通过日诱甄别(row) -> bool:
    """5条日诱剔除规则"""

    # ⚠️ 规则①：DXAB<己>负交+上破DJA+DJB
    日类BTAB = row.get("ZC日", 0)  # 近似：ZC=JZ-JC，AB=JA-JB，这里简化用
    日类BTZA = row.get("ZA日", 0)
    日类BTZB = row.get("ZB日", 0)
    if 日类BTAB <= 0 and 日类BTZA > 0 and 日类BTZB > 0:
        return False  # 规则①命中

    # ⚠️ 规则②：主升后连阴回归DJA+大阳柱
    柱排 = str(row.get("柱排日", ""))
    if "跌" in 柱排 and "连" in 柱排:
        # 需要判断前N天是否有连阴模式
        pass  # 简化版，后续可加强

    # ⚠️ 规则③：DJA之上连阴/阴阳阴+鼎
    鼎 = row.get("鼎日", 0)
    if 鼎 == 1 and ("连" in 柱排 or "阴" in 柱排):
        return False  # 规则③命中

    # ⚠️ 规则④：下破DJA须等再站上
    if 日类BTZA <= 0:
        return False  # 规则④命中

    # ⚠️ 规则⑤：反复震荡（简化：DTZA<3视为未站稳）
    if 日类BTZA < 3:
        # 需要额外判断
        pass

    return True


