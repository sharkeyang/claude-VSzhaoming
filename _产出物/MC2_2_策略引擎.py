def 基仓策略(row, 状态: 持仓状态) -> 操作:
    """策略一：基仓长期持有"""
    操作_ = 操作.不动
    当前价 = row.get("收盘价", 0)

    # --- 入场 ---
    if not 状态.有基仓:
        if 通过前置过滤(row) and 当前价 > 0:
            状态.有基仓 = True
            状态.基仓量 = 基仓量
            状态.基仓买入价 = 当前价
            状态.持仓市值 += 基仓量
            状态.当前资金 -= 基仓量
            操作_ = 操作.买入基仓
            状态.log(f"基仓入场 @ {当前价:.2f}")
            return 操作_

    if not 状态.有基仓:
        return 操作.不动

    # --- DJED 日主战场（持有监控） ---
    if row["DJED安心"]:
        if row["周级预警"]:
            状态.log("周级预警，保持观望")
        状态.基仓持仓天数 += 1
        return 操作.不动

    # --- 止盈 ---
    HA偏 = row.get("HA偏", 0)
    if HA偏 is not None and HA偏 >= 6:
        操作_ = 操作.止盈部分
        状态.log(f"止盈触发：HA偏={HA偏} @ {当前价:.2f}")

    # --- 止损①：DJE跌破 → 全部清仓 ---
    if not row["今日DXZE>0"]:
        if 状态.基仓买入价 > 0 and 当前价 > 0:
            盈亏 = 计算持仓盈亏(状态.基仓买入价, 当前价, 基仓量)
            记录交易(状态, "基仓止损DJE", 状态.基仓买入价, 当前价, 基仓量, 盈亏)
            状态.log(f"DJE跌破，基仓清仓 @ {当前价:.2f} 盈亏:{盈亏:.0f}")
        状态.有基仓 = False
        状态.基仓量 = 0
        状态.持仓市值 = 0
        操作_ = 操作.基仓清仓
        return 操作_

    # --- 止损②：WXZC<0（终极止损）---
    if row["ZC周"] is not None and row["ZC周"] < 0:
        if 状态.基仓买入价 > 0 and 当前价 > 0:
            盈亏 = 计算持仓盈亏(状态.基仓买入价, 当前价, 基仓量)
            记录交易(状态, "基仓止损WXZC", 状态.基仓买入价, 当前价, 基仓量, 盈亏)
            状态.log(f"WXZC<0，基仓止损 @ {当前价:.2f} 盈亏:{盈亏:.0f}")
        状态.有基仓 = False
        状态.基仓量 = 0
        状态.持仓市值 = 0
        操作_ = 操作.基仓清仓
        return 操作_

    状态.基仓持仓天数 += 1
    return 操作_


def 下周冲高策略(row, 状态: 持仓状态, 序列索引: int) -> 操作:
    """策略二：下周冲高（周五触发）"""
    if not 状态.有基仓:
        return 操作.不动

    row_date = row.get("日期")
    if row_date and row_date.weekday() != 4:
        return 操作.不动

    if not row.get("柱排周_升开头", False):
        return 操作.不动
    if not row.get("冲高有信号", False):
        return 操作.不动
    if 状态.有下周冲高:
        return 操作.不动

    当前价 = row.get("收盘价", 0)
    if 当前价 <= 0:
        return 操作.不动

    状态.有下周冲高 = True
    状态.下周冲高买入价 = 当前价
    状态.下周冲高买入日 = row["日期"]
    状态.下周冲高量 = 下周冲高仓
    状态.下周最高价 = 当前价
    状态.下周已止盈 = False
    状态.当前资金 -= 下周冲高仓
    操作_ = 操作.买入下周冲高
    状态.log(f"下周冲高入场 @ {当前价:.2f}")
    return 操作_


def 下周冲高出场监控(row, 状态: 持仓状态) -> 操作:
    """策略二出场监控

    买入后下周任意交易日：
    + 只要出现正收益 → 取最高点止盈
    - 下破 DJB → 止损
    - 周五无论盈亏平仓
    """
    if not 状态.有下周冲高:
        return 操作.不动

    当前价 = row.get("收盘价", 0)
    if 当前价 <= 0:
        return 操作.不动

    # 追踪买入后的最高价
    状态.下周最高价 = max(状态.下周最高价, 当前价)

    # 下一个周五前，如果有正收益就取最高点止盈
    row_date = row.get("日期")
    if 状态.下周冲高买入日 != row_date:  # 不是买入当天
        if 状态.下周最高价 > 状态.下周冲高买入价 and not 状态.下周已止盈:
            # 出现正收益 → 按最高点止盈（取过往最高价）
            最高盈亏 = (状态.下周最高价 - 状态.下周冲高买入价) / 状态.下周冲高买入价 * 状态.下周冲高量
            记录交易(状态, "下周止盈", 状态.下周冲高买入价, 状态.下周最高价,
                     状态.下周冲高量, 最高盈亏)
            状态.log(f"下周冲高止盈 @ {状态.下周最高价:.2f} 盈亏:{最高盈亏:.0f}")
            状态.下周已止盈 = True
            状态.有下周冲高 = False
            return 操作.平浮仓

    # DJB止损
    if row["ZB日"] is not None and row["ZB日"] < 0:
        if 状态.下周冲高买入价 > 0 and 当前价 > 0:
            盈亏 = 计算持仓盈亏(状态.下周冲高买入价, 当前价, 状态.下周冲高量)
            记录交易(状态, "下周止损DJB", 状态.下周冲高买入价, 当前价,
                     状态.下周冲高量, 盈亏)
            状态.log(f"下周止损 @ {当前价:.2f} 盈亏:{盈亏:.0f}")
        状态.有下周冲高 = False
        return 操作.平浮仓

    # 周五到期平仓
    if row_date and row_date.weekday() == 4:
        if 状态.下周冲高买入价 > 0 and 当前价 > 0:
            盈亏 = 计算持仓盈亏(状态.下周冲高买入价, 当前价, 状态.下周冲高量)
            记录交易(状态, "下周到期", 状态.下周冲高买入价, 当前价,
                     状态.下周冲高量, 盈亏)
            状态.log(f"下周到期 @ {当前价:.2f} 盈亏:{盈亏:.0f}")
        状态.有下周冲高 = False
        return 操作.平浮仓

    return 操作.不动


def 下日冲高策略(row, 状态: 持仓状态) -> 操作:
    """策略三：下日冲高（周中触发）"""
    if not 状态.有基仓:
        return 操作.不动

    row_date = row.get("日期")
    if row_date and row_date.weekday() == 4:
        return 操作.不动

    if 状态.有下日冲高:
        return 操作.不动

    当前价 = row.get("收盘价", 0)
    if 当前价 <= 0:
        return 操作.不动

    HA偏 = row.get("HA偏", 0)
    if HA偏 is not None and HA偏 >= 6:
        return 操作.不动

    if not row.get("柱排日_升开头", False):
        return 操作.不动
    if row["ZA日"] is None or row["ZA日"] <= 0:
        return 操作.不动
    if row["ZA日"] < 3:
        return 操作.不动
    if not 通过日诱甄别(row):
        return 操作.不动

    状态.有下日冲高 = True
    状态.下日冲高买入价 = 当前价
    状态.下日冲高买入日 = row["日期"]
    状态.下日冲高量 = 下日冲高仓
    状态.当前资金 -= 下日冲高仓
    状态.持仓市值 += 下日冲高仓
    操作_ = 操作.买入下日冲高
    状态.log(f"下日冲高入场 @ {当前价:.2f}")
    return 操作_


def 下日冲高出场监控(row, 状态: 持仓状态) -> 操作:
    """策略三出场监控"""
    if not 状态.有下日冲高:
        return 操作.不动

    当前价 = row.get("收盘价", 0)

    if row["ZD日"] is not None and row["ZD日"] < 0:
        if 状态.下日冲高买入价 > 0 and 当前价 > 0:
            盈亏 = 计算持仓盈亏(状态.下日冲高买入价, 当前价, 状态.下日冲高量)
            记录交易(状态, "下日冲高止损", 状态.下日冲高买入价, 当前价,
                     状态.下日冲高量, 盈亏)
            状态.log(f"下日冲高止损 @ {当前价:.2f} 盈亏:{盈亏:.0f}")
        状态.有下日冲高 = False
        状态.持仓市值 -= 状态.下日冲高量
        return 操作.平浮仓

    # 次日涨幅≥3%止盈
    涨幅 = row.get("涨幅", 0)
    if 涨幅 is not None and 涨幅 >= 3:
        if 状态.下日冲高买入价 > 0 and 当前价 > 0:
            盈亏 = 计算持仓盈亏(状态.下日冲高买入价, 当前价, 状态.下日冲高量)
            记录交易(状态, "下日冲高止盈", 状态.下日冲高买入价, 当前价,
                     状态.下日冲高量, 盈亏)
            状态.log(f"下日冲高止盈 @ {当前价:.2f} 盈亏:{盈亏:.0f}")
        状态.有下日冲高 = False
        状态.持仓市值 -= 状态.下日冲高量
        return 操作.平浮仓

    return 操作.不动


# ============================================================
# 回测引擎
# ============================================================

def 运行回测(df: pd.DataFrame, 股票名: str = "未知") -> dict:
    """
    在DataFrame上运行三层策略回测
    """
    状态 = 持仓状态()
    所有操作 = []

    for idx, row in df.iterrows():
        if idx == 0:
            状态.每日净值.append(状态.初始资金)
            continue

        当日操作 = []

        # 净值计算：现金 + 持仓市值
        # 基仓市值 = 基仓量 * (当前收盘价 / 基仓买入价)（如果基仓买入价=0，用初始价）
        if 状态.有基仓 and 状态.基仓买入价 > 0 and row.get("收盘价", 0) > 0:
            基仓市值 = 基仓量 * row["收盘价"] / 状态.基仓买入价
        else:
            基仓市值 = 0
        # 下周冲高市值
        if 状态.有下周冲高 and 状态.下周冲高买入价 > 0 and row.get("收盘价", 0) > 0:
            下周市值 = 状态.下周冲高量 * row["收盘价"] / 状态.下周冲高买入价
        else:
            下周市值 = 0
        # 下日冲高市值
        if 状态.有下日冲高 and 状态.下日冲高买入价 > 0 and row.get("收盘价", 0) > 0:
            下日市值 = 状态.下日冲高量 * row["收盘价"] / 状态.下日冲高买入价
        else:
            下日市值 = 0

        状态.持仓市值 = 基仓市值 + 下周市值 + 下日市值
        总资产 = 状态.当前资金 + 状态.持仓市值
        状态.每日净值.append(总资产)

        # 基仓策略
        op = 基仓策略(row, 状态)
        if op != 操作.不动:
            当日操作.append(op)

        # 下周冲高
        op = 下周冲高策略(row, 状态, idx)
        if op != 操作.不动:
            当日操作.append(op)

        # 下周冲高出场
        op = 下周冲高出场监控(row, 状态)
        if op != 操作.不动:
            当日操作.append(op)

        # 下日冲高
        op = 下日冲高策略(row, 状态)
        if op != 操作.不动:
            当日操作.append(op)

        # 下日冲高出场
        op = 下日冲高出场监控(row, 状态)
        if op != 操作.不动:
            当日操作.append(op)

        if 当日操作:
            所有操作.append({"日期": row["日期"], "操作": 当日操作, "状态": str(状态)})

    总资产 = 状态.当前资金 + 状态.持仓市值
    净盈亏 = 总资产 - 状态.初始资金
    总收益率 = 净盈亏 / 状态.初始资金 if 状态.初始资金 > 0 else 0

    统计 = {
        "标的": 股票名,
        "数据天数": len(df),
        "初始资金": 状态.初始资金,
        "最终资产": 总资产,
        "净盈亏": 净盈亏,
        "总收益率": 总收益率,
        "交易次数": 状态.交易次数,
        "盈利次数": 状态.盈利次数,
        "总买入次数": sum(1 for o in 所有操作 if 操作.买入基仓 in o["操作"] or 操作.买入下周冲高 in o["操作"] or 操作.买入下日冲高 in o["操作"]),
        "基仓最后状态": "持有" if 状态.有基仓 else "空仓",
        "交易日志": 状态.操作日志[-30:] if 状态.操作日志 else [],
        "交易记录": 状态.交易记录,
        "详细操作": 所有操作[-10:],
        "每日净值": 状态.每日净值,
        "收盘价序列": df["收盘价"].tolist(),
    }

    return 统计


