# ============================================================
# P&L 计算辅助
# ============================================================

def 计算持仓盈亏(买入价: float, 当前价: float, 持仓金额: int) -> float:
    """计算一笔持仓的浮动盈亏"""
    if 买入价 <= 0:
        return 0.0
    return (当前价 - 买入价) / 买入价 * 持仓金额


def 记录交易(状态, 类型: str, 买入价: float, 卖出价: float,
             数量: int, 盈亏: float):
    """记录一笔已完成的交易"""
    # 卖出后，本金 + 盈亏 一起回到现金
    回收资金 = 数量 + 盈亏
    状态.交易记录.append({
        "类型": 类型,
        "买入价": 买入价,
        "卖出价": 卖出价,
        "数量": 数量,
        "盈亏": 盈亏,
        "回收资金": 回收资金,
    })
    状态.已实现盈亏 += 盈亏
    状态.交易次数 += 1
    if 盈亏 > 0:
        状态.盈利次数 += 1
    状态.当前资金 += 回收资金
    if 状态.持仓市值 > 0:
        状态.持仓市值 -= 数量


