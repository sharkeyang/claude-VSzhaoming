# M3.3 下日冲高独立回测
# ============================================================

def 下日冲高独立回测(df: pd.DataFrame, 股票名: str = "未知", 初始资金=50000) -> dict:
    """只看下日冲高策略，独立交易（不依赖基仓）"""
    状态 = 持仓状态()
    状态.初始资金 = 初始资金
    状态.当前资金 = 初始资金

    for idx, row in df.iterrows():
        if idx == 0:
            状态.每日净值.append(状态.初始资金)
            continue

        # 净值
        if 状态.有下日冲高 and 状态.下日冲高买入价 > 0 and row.get("收盘价", 0) > 0:
            下日市值 = 状态.下日冲高量 * row["收盘价"] / 状态.下日冲高买入价
        else:
            下日市值 = 0
        总资产 = 状态.当前资金 + 下日市值
        状态.每日净值.append(总资产)

        # 策略三入场/出场（不依赖基仓）
        if not 状态.有下日冲高:
            row_date = row.get("日期")
            if row_date and row_date.weekday() == 4:
                pass  # 周五不走
            else:
                当前价 = row.get("收盘价", 0)
                HA偏 = row.get("HA偏", 0)
                if (当前价 > 0 and row.get("柱排日_升开头", False)
                        and row["ZA日"] is not None and row["ZA日"] >= 3
                        and not (HA偏 is not None and HA偏 >= 6)
                        and 通过日诱甄别(row)):
                    状态.有下日冲高 = True
                    状态.下日冲高买入价 = 当前价
                    状态.下日冲高买入日 = row["日期"]
                    状态.下日冲高量 = 下日冲高仓
                    状态.当前资金 -= 下日冲高仓

        # 出场
        if 状态.有下日冲高:
            当前价 = row.get("收盘价", 0)
            出场 = False
            if row["ZD日"] is not None and row["ZD日"] < 0:
                盈亏 = (当前价 - 状态.下日冲高买入价) / 状态.下日冲高买入价 * 状态.下日冲高量
                记录交易(状态, "下日止损", 状态.下日冲高买入价, 当前价, 状态.下日冲高量, 盈亏)
                出场 = True
            elif row.get("涨幅", 0) is not None and row["涨幅"] >= 3:
                盈亏 = (当前价 - 状态.下日冲高买入价) / 状态.下日冲高买入价 * 状态.下日冲高量
                记录交易(状态, "下日止盈", 状态.下日冲高买入价, 当前价, 状态.下日冲高量, 盈亏)
                出场 = True
            if 出场:
                状态.有下日冲高 = False
                状态.下日冲高量 = 0

    总资产 = 状态.每日净值[-1] if 状态.每日净值 else 状态.初始资金  # 用净值序列最后一位
    净盈亏 = 总资产 - 状态.初始资金

    return {
        "标的": 股票名,
        "策略": "下日冲高独立",
        "初始资金": 状态.初始资金,
        "最终资产": 总资产,
        "净盈亏": 净盈亏,
        "总收益率": 净盈亏 / 状态.初始资金 if 状态.初始资金 > 0 else 0,
        "交易次数": 状态.交易次数,
        "盈利次数": 状态.盈利次数,
        "每日净值": 状态.每日净值,
        "交易记录": 状态.交易记录,
    }


