def 基仓独立回测(df: pd.DataFrame, 股票名: str = "未知") -> dict:
    """只看基仓策略，不带任何浮仓/冲高策略"""
    状态 = 持仓状态()
    净买次数 = 0

    for idx, row in df.iterrows():
        if idx == 0:
            状态.每日净值.append(状态.初始资金)
            continue

        # 净值
        if 状态.有基仓 and 状态.基仓买入价 > 0 and row.get("收盘价", 0) > 0:
            基仓市值 = 基仓量 * row["收盘价"] / 状态.基仓买入价
        else:
            基仓市值 = 0
        总资产 = 状态.当前资金 + 基仓市值
        状态.每日净值.append(总资产)

        # 仅基仓策略
        op = 基仓策略(row, 状态)
        if op == 操作.买入基仓:
            净买次数 += 1

    总资产 = 状态.每日净值[-1] if 状态.每日净值 else 状态.初始资金  # 用净值序列最后一位（含市值）
    净盈亏 = 总资产 - 状态.初始资金

    return {
        "标的": 股票名,
        "策略": "基仓独立",
        "初始资金": 状态.初始资金,
        "最终资产": 总资产,
        "净盈亏": 净盈亏,
        "总收益率": 净盈亏 / 状态.初始资金 if 状态.初始资金 > 0 else 0,
        "交易次数": 状态.交易次数,
        "盈利次数": 状态.盈利次数,
        "基仓状态": "持有" if 状态.有基仓 else "空仓",
        "净买次数": 净买次数,
        "每日净值": 状态.每日净值,
        "交易记录": 状态.交易记录,
        "操作日志": 状态.操作日志[-20:],
    }


