# M3.2 下周冲高独立回测
# ============================================================

def 下周冲高独立回测(df: pd.DataFrame, 股票名: str = "未知", 初始资金=50000) -> dict:
    """只看下周冲高策略，独立交易

    周五买入后，下周内任意交易日：
    + 出现正收益 → 取最高点止盈
    - 下破DJB → 止损
    - 周五无论盈亏平仓
    """
    状态 = 持仓状态()
    状态.初始资金 = 初始资金
    状态.当前资金 = 初始资金

    for idx, row in df.iterrows():
        if idx == 0:
            状态.每日净值.append(状态.初始资金)
            continue

        # 净值
        if 状态.有下周冲高 and 状态.下周冲高买入价 > 0 and row.get("收盘价", 0) > 0:
            下周市值 = 状态.下周冲高量 * row["收盘价"] / 状态.下周冲高买入价
        else:
            下周市值 = 0
        总资产 = 状态.当前资金 + 下周市值
        状态.每日净值.append(总资产)

        row_date = row.get("日期")
        当前价 = row.get("收盘价", 0) or 0

        # 入场：周五 + 条件
        if not 状态.有下周冲高 and row_date and row_date.weekday() == 4:
            if row.get("柱排周_升开头", False) and row.get("冲高有信号", False) and 当前价 > 0:
                状态.有下周冲高 = True
                状态.下周冲高买入价 = 当前价
                状态.下周冲高买入日 = row_date
                状态.下周冲高量 = 下周冲高仓
                状态.下周最高价 = 当前价
                状态.当前资金 -= 下周冲高仓

        # 出场
        if 状态.有下周冲高 and 当前价 > 0:
            状态.下周最高价 = max(状态.下周最高价, 当前价)
            出场 = False

            # 止盈：不是买入当天，且最高价>买入价
            if 状态.下周冲高买入日 != row_date and 状态.下周最高价 > 状态.下周冲高买入价:
                盈亏 = (状态.下周最高价 - 状态.下周冲高买入价) / 状态.下周冲高买入价 * 状态.下周冲高量
                记录交易(状态, "下周止盈", 状态.下周冲高买入价, 状态.下周最高价,
                         状态.下周冲高量, 盈亏)
                出场 = True

            # 止损：下破DJB
            elif row["ZB日"] is not None and row["ZB日"] < 0:
                盈亏 = (当前价 - 状态.下周冲高买入价) / 状态.下周冲高买入价 * 状态.下周冲高量
                记录交易(状态, "下周止损", 状态.下周冲高买入价, 当前价,
                         状态.下周冲高量, 盈亏)
                出场 = True

            # 周五到期平仓（不是买入当天）
            elif row_date and row_date.weekday() == 4 and 状态.下周冲高买入日 != row_date:
                盈亏 = (当前价 - 状态.下周冲高买入价) / 状态.下周冲高买入价 * 状态.下周冲高量
                记录交易(状态, "下周到期", 状态.下周冲高买入价, 当前价,
                         状态.下周冲高量, 盈亏)
                出场 = True

            if 出场:
                状态.有下周冲高 = False
                状态.下周冲高量 = 0

    总资产 = 状态.每日净值[-1] if 状态.每日净值 else 状态.初始资金  # 用净值序列最后一位
    净盈亏 = 总资产 - 状态.初始资金

    return {
        "标的": 股票名,
        "策略": "下周冲高独立",
        "初始资金": 状态.初始资金,
        "最终资产": 总资产,
        "净盈亏": 净盈亏,
        "总收益率": 净盈亏 / 状态.初始资金 if 状态.初始资金 > 0 else 0,
        "交易次数": 状态.交易次数,
        "盈利次数": 状态.盈利次数,
        "每日净值": 状态.每日净值,
        "交易记录": 状态.交易记录,
    }


