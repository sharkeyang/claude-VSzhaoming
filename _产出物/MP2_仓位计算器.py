"""
MP2 组合仓位计算器 — 根据四域区划+板块+当前持仓计算调整方案

约束规则：
- 单板块 ≤ 30%
- 单标的 ≤ 15%
- _多长基仓 40~60%，<周浮仓> 15~25%，<日浮仓> 5~10%
- ETF 15~25%，现金 5~10%
"""

from dataclasses import dataclass, field
from typing import List, Dict


@dataclass
class 持仓项:
    代码: str
    名称: str
    板块: str
    区划: str          # 多长/多被/空长/空看
    仓位比例: float    # 0~100
    策略: str = ""     # 长基仓/周浮仓/日浮仓


@dataclass
class 调整建议:
    代码: str
    当前仓位: float
    建议仓位: float
    操作: str          # 增仓/减仓/清仓/持有
    原因: str


def 计算组合调整(持仓列表: List[持仓项]) -> List[调整建议]:
    """
    计算持仓调整方案
    """
    调整项 = []
    板块仓位 = {}

    # 1. 计算各板块仓位
    for 持仓 in 持仓列表:
        板块仓位[持仓.板块] = 板块仓位.get(持仓.板块, 0) + 持仓.仓位比例

    # 2. 逐项检查
    for 持仓 in 持仓列表:
        # 区划检查
        if 持仓.区划 == "空长":
            调整项.append(调整建议(
                代码=持仓.代码, 当前仓位=持仓.仓位比例,
                建议仓位=0, 操作="清仓", 原因="区划=_空长，强制退出"))
        elif 持仓.区划 == "空看":
            调整项.append(调整建议(
                代码=持仓.代码, 当前仓位=持仓.仓位比例,
                建议仓位=0, 操作="清仓", 原因="区划=_空看，不参与"))
        elif 持仓.区划 == "多被":
            调整项.append(调整建议(
                代码=持仓.代码, 当前仓位=持仓.仓位比例,
                建议仓位=持仓.仓位比例 * 0.5, 操作="减仓",
                原因="区划=_多被，仅小仓位参与"))

        # 板块超限检查（仅对多长/多被标的）
        if 持仓.区划 in ("多长", "多被"):
            板块仓位比 = 板块仓位.get(持仓.板块, 0)
            if 板块仓位比 > 30:
                超额 = 板块仓位比 - 30
                调整项.append(调整建议(
                    代码=持仓.代码, 当前仓位=持仓.仓位比例,
                    建议仓位=持仓.仓位比例 - 超额 * 0.5,
                    操作="减仓", 原因=f"板块{持仓.板块}仓位{板块仓位比}%超限30%"))

    return 调整项


def 生成仓位报告(持仓列表: List[持仓项], 调整项: List[调整建议]) -> str:
    """生成可读的仓位报告"""
    报告 = []
    报告.append("=== 组合仓位报告 ===\n")
    for 持仓 in 持仓列表:
        报告.append(f"{持仓.名称}({持仓.代码}) [{持仓.板块}] 区划={持仓.区划} 仓位={持仓.仓位比例}%")
    for 调整 in 调整项:
        报告.append(f"  → {调整.操作} {调整.代码}: {调整.当前仓位}%→{调整.建议仓位}%（{调整.原因}）")
    return "\n".join(报告)